# Now we're starting to build our ml model
# # Handeling the object columns by label encoder

from sklearn.preprocessing import LabelEncoder

le = LabelEncoder()
for i in df.columns:
  if df[i].dtype == "object":
    df[i] = le.fit_transform(df[i])

# checking object is successfully handeled
df.info()

# Now WE'RE BULDING OUR LR MODEL

# SPLIT THE DATA INDEPENDENT(X) AND DEPENDENT(Y)

x = df.drop(columns = ['Life expectancy ', 'Year'])

x.columns

y = df['Life expectancy ']

y

# Split the data into train and test

from sklearn.model_selection import train_test_split

x_train,x_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state= 42 )

print(x_train.shape)
print(x_test.shape)

# Train the model

from sklearn.linear_model import LinearRegression

model = LinearRegression()

model.fit(x_train,y_train)

#Shows which features impact life expectancy most
print('Intercept:', model.intercept_)
print('Coefficients:', pd.Series(model.coef_, index=x.columns).sort_values())

# Test the model

y_pred = model.predict(x_test)

y_pred[:5]

# Evaluation ->

from sklearn.metrics import r2_score, mean_absolute_error, mean_squared_error

print(f'R² Score  : {r2_score(y_test, y_pred):.4f}')
print(f'MAE       : {mean_absolute_error(y_test, y_pred):.4f}')
print(f'RMSE      : {np.sqrt(mean_squared_error(y_test, y_pred)):.4f}')

