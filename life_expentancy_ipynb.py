# Checking skewness to decide mean vs median for null imputation
# abs(skew) > 0.5 = median, else = mean

df.skew(numeric_only = True)

# Checking for null/empty values
df.isnull().sum()

# Handing the null / empty values by automating
# if skewness > 0.5 fill with median else mean
# if data type is in string(object) fillna with mode

for i in df.columns:
  if df[i].isnull().sum() >0 :
    if df[i].dtype == "object":
      df[i] = df[i].fillna(df[i].mode()[0])
    elif abs(df[i].skew()) >0.5:
      df[i] = df.groupby('Country')[i].transform(lambda x: x.fillna(x.median()))
      df[i] = df[i].fillna(df[i].median())
    else:
      df[i] = df.groupby("Country")[i].transform(lambda x: x.fillna(x.mean()))
      df[i] = df[i].fillna(df[i].mean())

